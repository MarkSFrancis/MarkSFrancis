namespace MarkSFrancis.Drawing
{
    /// <summary>
    /// An RGB color
    /// </summary>
    public struct Color
    {
        /// <summary>
        /// The amount of red in this color
        /// </summary>
        public byte Red { get; set; }

        /// <summary>
        /// The amount of green in this color
        /// </summary>
        public byte Green { get; set; }

        /// <summary>
        /// The amount of blue in this color
        /// </summary>
        public byte Blue { get; set; }
    }
}
